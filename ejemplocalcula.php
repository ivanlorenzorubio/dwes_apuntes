 * @author cic
 */
class Calcula{
    /**
     * suma dos numeros
     * @param mixed $a
     * @param mixed $b
     * 
     * @return [float]
     */
    public function suma($a,$b){
        return $a+$b;
    }

    /**
     * resta dos numeros
     * @param mixed $a
     * @param mixed $b
     * 
     * @return [float]
     */
    public function resta($a,$b){
        return $a-$b;
    }
}
?>